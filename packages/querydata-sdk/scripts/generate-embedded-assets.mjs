import { mkdirSync, readFileSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const packageRoot = path.resolve(__dirname, '..');
const assetsDir = path.join(packageRoot, 'assets');
const outputDir = path.join(packageRoot, 'src', 'generated');
const outputFile = path.join(outputDir, 'embedded-assets.ts');

const assetFiles = [
  'fineui-base.min.js',
  'BICst.js',
  'i18n.js',
  'static.min.js',
];

const embeddedScripts = assetFiles.map(fileName => {
  const assetPath = path.join(assetsDir, fileName);
  return readFileSync(assetPath, 'utf8');
});

const moduleSource = `// Auto-generated by scripts/generate-embedded-assets.mjs. Do not edit manually.
export const embeddedDependencyScripts: readonly string[] = ${JSON.stringify(embeddedScripts, null, 2)};
`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputFile, moduleSource, 'utf8');

console.log(`[FineBI SDK] Generated embedded asset module: ${path.relative(packageRoot, outputFile)}`);
